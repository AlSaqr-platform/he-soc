void mod_exp(uint8_t* result, const uint8_t* base, const uint8_t* exponent, const uint8_t* modulus) {
    const int key_size = 512;

    // Initialize result to 1
    for (int i = 0; i < TITANSSL_PAYLOAD_SIZE; i++) {
        result[i] = 0;
    }
    result[TITANSSL_PAYLOAD_SIZE - 1] = 1;

    // Perform modular exponentiation
    for (int i = RSA - 1; i >= 0; i--) {
        // Square the result
        for (int j = 0; j < TITANSSL_PAYLOAD_SIZE; j++) {
            result[j] = (result[j] * result[j]) % modulus[j];
        }

        // Multiply by base if the current bit of the exponent is set
        if ((exponent[i / 8] >> (i % 8)) & 1) {
            for (int j = 0; j < TITANSSL_PAYLOAD_SIZE; j++) {
                result[j] = (result[j] * base[j]) % modulus[j];
            }
        }
    }
}

