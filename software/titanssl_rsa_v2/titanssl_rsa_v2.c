void mod_mult(uint8_t* result, uint8_t* a, uint8_t* b, uint8_t* mod) {
    uint8_t temp[2 * TITANSSL_PAYLOAD_SIZE];
    memset(temp, 0, 2 * TITANSSL_PAYLOAD_SIZE);

    for (int i = 0; i < TITANSSL_PAYLOAD_SIZE; i++) {
        for (int j = 0; j < TITANSSL_PAYLOAD_SIZE; j++) {
            temp[i + j] += a[i] * b[j];
            temp[i + j + 1] += temp[i + j] / 256;
            temp[i + j] %= 256;
        }
    }

    for (int i = 0; i < TITANSSL_PAYLOAD_SIZE; i++) {
        result[i] = temp[i];
    }

    // Perform modular reduction
    for (int i = 2 * TITANSSL_PAYLOAD_SIZE - 1; i >= TITANSSL_PAYLOAD_SIZE; i--) {
        if (temp[i] != 0) {
            for (int j = TITANSSL_PAYLOAD_SIZE - 1; j >= 0; j--) {
                temp[i - TITANSSL_PAYLOAD_SIZE + j] -= mod[j];
                if (temp[i - TITANSSL_PAYLOAD_SIZE + j] < 0) {
                    temp[i - TITANSSL_PAYLOAD_SIZE + j] += 256;
                    temp[i - TITANSSL_PAYLOAD_SIZE + j - 1]--;
                }
            }
        }
    }

    for (int i = 0; i < TITANSSL_PAYLOAD_SIZE; i++) {
        result[i] = temp[i];
    }
}

void mod_exp(uint8_t* result, uint8_t* base, uint8_t* exp, uint8_t* mod) {
    uint8_t temp[TITANSSL_PAYLOAD_SIZE];
    uint8_t one[TITANSSL_PAYLOAD_SIZE];
    memset(one, 0, TITANSSL_PAYLOAD_SIZE);
    one[TITANSSL_PAYLOAD_SIZE - 1] = 1;

    memcpy(result, one, TITANSSL_PAYLOAD_SIZE);

    for (int i = 0; i < RSA; i++) {
        if (exp[i / 8] & (1 << (i % 8))) {
            mod_mult(temp, result, base, mod);
            memcpy(result, temp, TITANSSL_PAYLOAD_SIZE);
        }
        mod_mult(temp, base, base, mod);
        memcpy(base, temp, TITANSSL_PAYLOAD_SIZE);
    }
}

// Function to perform RSA encryption
void rsa_encrypt(uint8_t* plaintext, uint8_t* modulus, uint8_t* exponent, uint8_t* ciphertext) {
    mod_exp(ciphertext, plaintext, exponent, modulus);
}

// Function to perform RSA decryption
void rsa_decrypt(uint8_t* ciphertext, uint8_t* modulus, uint8_t* exponent, uint8_t* decrypted_text) {
    mod_exp(decrypted_text, ciphertext, exponent, modulus);
}

